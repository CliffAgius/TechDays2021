function getMessage(cb) {

    console.log("Getting flight data");

    data = flightData[messageId];

    cb(JSON.stringify({
        messageId: messageId,
        deviceId: 'Raspberry Pi Flight Simulator',
        callSign: data.callSign,
        date: data.date,
        time: data.time,
        latitude: data.latitude,
        longitude: data.longitude,
        altitude: data.altitude,
        vSFPM: data.vSFPM,
        secondsLastReport: data.secondsLastReport,
        speed: data.speed,
        direction: data.direction,
        outSideAirTemp: data.outSideAirTemp,
        windDirection: data.windDirection,
        windSpeed: data.windSpeed
    }));

    messageId++;

}

function sendMessage() {
    if (!sendingMessage) { return; }

    getMessage(function (content) {
        var message = new Message(content);
        console.log('Sending message: ' + content);
        client.sendEvent(message, function (err) {
            if (err) {
                console.error('Failed to send message to Azure IoT Hub');
            } else {
                console.log('Message sent to Azure IoT Hub');
            }
        });
    });
}

function onStart(request, response) {
    console.log('Try to invoke method start(' + request.payload + ')');
    sendingMessage = true;

    response.send(200, 'Successfully started sending message to cloud', function (err) {
        if (err) {
            console.error('[IoT hub Client] Failed sending a method response:\n' + err.message);
        }
    });
}

function onStop(request, response) {
    console.log('Try to invoke method stop(' + request.payload + ')');
    sendingMessage = false;

    response.send(200, 'Successfully stopped sending message to cloud', function (err) {
        if (err) {
            console.error('[IoT hub Client] Failed sending a method response:\n' + err.message);
        }
    });
}

function receiveMessageCallback(msg) {
    var message = msg.getData().toString('utf-8');
    client.complete(msg, function () {
        console.log('Receive message: ' + message);
    });
}

sendingMessage = true;

// create a client
client = Client.fromConnectionString(connectionString, Protocol);

client.open(function (err) {
    if (err) {
        console.error('[IoT hub Client] Connect error: ' + err.message);
        return;
    }

    console.log("Welcome to TechDays 2021 - Flight into IoT!");

    // set C2D and device method callback
    client.onDeviceMethod('start', onStart);
    client.onDeviceMethod('stop', onStop);
    client.on('message', receiveMessageCallback);
    setInterval(sendMessage, 2000);
});
